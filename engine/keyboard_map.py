"""
Карта соседних клавиш раскладки ЙЦУКЕН для генерации реалистичных опечаток.
"""

JCUKEN_ADJACENT: dict[str, list[str]] = {
    # Верхний ряд
    'й': ['ц', 'ф'],
    'ц': ['й', 'у', 'ф', 'ы'],
    'у': ['ц', 'к', 'ы', 'в'],
    'к': ['у', 'е', 'в', 'а'],
    'е': ['к', 'н', 'а', 'п'],
    'н': ['е', 'г', 'п', 'р'],
    'г': ['н', 'ш', 'р', 'о'],
    'ш': ['г', 'щ', 'о', 'л'],
    'щ': ['ш', 'з', 'л', 'д'],
    'з': ['щ', 'х', 'д', 'ж'],
    'х': ['з', 'ъ', 'ж', 'э'],
    'ъ': ['х', 'э'],
    # Средний ряд
    'ф': ['й', 'ц', 'я', 'ы'],
    'ы': ['ц', 'у', 'ф', 'в', 'я', 'ч'],
    'в': ['у', 'к', 'ы', 'а', 'ч', 'с'],
    'а': ['к', 'е', 'в', 'п', 'с', 'м'],
    'п': ['е', 'н', 'а', 'р', 'м', 'и'],
    'р': ['н', 'г', 'п', 'о', 'и', 'т'],
    'о': ['г', 'ш', 'р', 'л', 'т', 'ь'],
    'л': ['ш', 'щ', 'о', 'д', 'ь', 'б'],
    'д': ['щ', 'з', 'л', 'ж', 'б', 'ю'],
    'ж': ['з', 'х', 'д', 'э', 'ю'],
    'э': ['х', 'ъ', 'ж'],
    # Нижний ряд
    'я': ['ф', 'ы', 'ч'],
    'ч': ['ы', 'в', 'я', 'с'],
    'с': ['в', 'а', 'ч', 'м'],
    'м': ['а', 'п', 'с', 'и'],
    'и': ['п', 'р', 'м', 'т'],
    'т': ['р', 'о', 'и', 'ь'],
    'ь': ['о', 'л', 'т', 'б'],
    'б': ['л', 'д', 'ь', 'ю'],
    'ю': ['д', 'ж', 'б'],
}


def get_adjacent_key(char: str) -> str | None:
    """Возвращает случайную соседнюю клавишу или None."""
    import random
    lower = char.lower()
    neighbors = JCUKEN_ADJACENT.get(lower)
    if not neighbors:
        return None
    replacement = random.choice(neighbors)
    return replacement.upper() if char.isupper() else replacement
