def get_markers_by_category(category: str) -> list[MarkerInfo]:
    return [m for m in MARKER_REGISTRY if m.category == category]


def get_marker_by_id(marker_id: str) -> MarkerInfo | None:
    for m in MARKER_REGISTRY:
        if m.id == marker_id:
            return m
    return None


def get_default_profile() -> dict[str, int]:
    """Возвращает словарь {marker_id: default_prob}."""
    return {m.id: m.default_prob for m in MARKER_REGISTRY}
